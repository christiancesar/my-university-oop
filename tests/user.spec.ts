import { User } from "src/user.js";

describe("User", () => {
  it("should create a new user", () => {
    const user = new User(
      "Jhon Doe",
      "jhon.doe@example.com",
      "123456"
    );
    console.log(user);
    expect(user).toEqual(expect.objectContaining({ name: "Jhon Doe" }));
  });
});
